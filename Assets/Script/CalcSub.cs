using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class CalcSub : MonoBehaviour
{
    public int Add(int a, int b) {
        return a + b;
    }

    public int Sub(int a, int b) {
        return a + b;
    }

    public int Mul(int a, int b) {
        return a * b;
    }

    public int Div(int a, int b) {
        return a / b;
    }
}
